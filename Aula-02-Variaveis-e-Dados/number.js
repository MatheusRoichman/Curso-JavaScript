// O tioo de dado Number se refere a valores numéricos (números inteiros, reais...). Serve paea fazer operações, principalmente aritméticas. Tem alguns valores internos:

// Int (número inteiro)
let num1 = 14;

// Float (número real)
let num2 = 3.14;

// Infinity (valor infinito)
let num3 = Infinity;

// NaN (Not a Number, ou seja, algo que não é um valor numérico sendo convertido para Number)
let num4 = Number("Texto?");

console.log(num1, num2, num3, num4);