// Jeito antigo de declarar variáveis
var dado1 = "Um dado";

// Jeito moderno (ES6) de declarar variáveis
let dado2 = "Outro dado";

// Jeito de declarar uma constante (dado imutável)
const dado3 = "Dado imutável"

console.log(dado1, dado2, dado3)

// Observação: Há diferença no uso de var e let, mas é preferível o uso de let.