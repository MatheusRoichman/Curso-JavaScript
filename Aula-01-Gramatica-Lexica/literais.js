/* Podemos definir um dado de 2 maneiras: declaração formal e declaração literal.
Na formal, declaramos o dado instanciando um objeto da classe correspondente do tipo de dado.
Na literal, declaramos o dado diretamente por sua sintaxe LITERAL. No caso de String (cadeia de caracteres), definimos ela delimitando o texto com aspas.
*/

// Declaração formal
var textoFormal = new String("Texto 1");

// Declaração literal
var textoLiteral = "Texto 2";

console.log(textoFormal, textoLiteral);