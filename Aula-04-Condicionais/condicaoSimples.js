let numero = Number(prompt("Digite um número"));

if (numero > 10) {
  console.log(`${numero} é maior que 10`);
} else {
  console.log(`${numero} não é maior que 10`);
};

// if (em português, significa "se") é a palavra chave para começar uma condicional. A condição deve ser colocada entre os parênteses.
// else é executado caso a condicional passada dê errado (ou seja, se a condição foi falsa).