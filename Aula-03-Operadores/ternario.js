let numero = 13;

// É usado para fazer uma condicional simples. 

// Primeiro operando: expressão a ser verificada.
// Segundo operando: operação a ser executada caso a expressão seja true.
// Terceiro operando: operação a ser executada caso a expressão seja false.

// Por ter 3 operandos, é chamado de operador ternário.
numero > 10 ? console.log("Número maior que 10") : console.log("Número menor que 10");