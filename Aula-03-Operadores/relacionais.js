// Todos esses retornas valores Boolean: true ou false.

console.log(3 < 5); // Menor que
console.log(3 <= 3); // Menor ou igual a

console.log(5 > 7); // Maior que
console.log(5 >= 3); // Maior ou igual a

console.log(5 == '5'); // Verifica se os 2 operandos têm valor igual, igmorando tipo de dado.
console.log(5 === '5'); // Verifica se os 2 operandos têm valor e tipo de dado iguais.

console.log(8 != '8'); // Verifica se os 2 operandos têm valor diferente, igmorando tipo de dado.
console.log(8 !== '8'); // Verifica se os 2 operandos têm valor e tipo de dados diferentes.