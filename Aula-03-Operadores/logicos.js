// Operador de negação ( ! ). Inverte um valor false para true e vice-versa.
console.log(!false);

// Conjunção ( && ). Retorna true se seus 2 operandos forem expressões verdadeiras. Caso contrário, retorna false.
console.log(2 < 3 && 3 < 5);

// Disjunção ( || ). Retorna true se um dos seus 2 operandos (ou os 2) forem expressões verdadeiras. Caso contrário, retorna false.
console.log(2 === '2' || 2 == '2');