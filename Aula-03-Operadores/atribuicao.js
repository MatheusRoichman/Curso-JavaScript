let numero = 6; 
// Atribuição simples. Dá um valor para algo.
console.log(numero);

numero += 3;
// Auto-atribuição. Opera em conjunto com operafores aritméticos.
// Funcionamento no exemplo: o novo valor da variável será o valor antigo somado com 3 (somado pois antes do = vem o +).
console.log(numero);

numero++;
// Incremento, adiciona 1 ao valor.
console.log(numero);

numero--;
// Decremento, subtrai 1 do valor.
console.log(numero);