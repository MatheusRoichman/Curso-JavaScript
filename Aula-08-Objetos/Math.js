// O objeto Math trabalha com valores numéricos, executando funções matemáticas.

// Arredondar um número para o maior número inteiro próximo
console.log(Math.ceil(7.7));

// Arredondar um número para o menor número inteiro próximo
console.log(Math.floor(7.3));

// Raiz quadrada
console.log(Math.sqrt(49));

// Raiz cúbica
console.log(Math.cbrt(216));

// Potenciação
console.log(Math.pow(6, 3));

// Achar o maior número numa lista
console.log(Math.max(3, 7, 9, 3, 1));

// Achar o menor número numa lista
console.log(Math.min(3, 7, 9, 3, 1));