// Retorno é um valor que a função retorna para os parâmetros recebidos.
// Pense como se a função fosse uma loja, os parâmetros fossem seu dinheiro e o retorno os produtos comprados.
function subtraiDois(num1, num2) {
  return num1 - num2;
}

console.log(subtraiDois(5, 3));

const multiplicarDois = function(num1, num2) {
  return num1 * num2;
};

console.log(multiplicarDois(4, 2))